    public void loadTable() {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Jabatan");

        try {
            Connection c = koneksi.getConnection();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery("SELECT * FROM user");

            while (r.next()) {
                model.addRow(new Object[]{
                    r.getInt("id"),
                    r.getString("nama"),
                    r.getString("username"),
                    r.getString("password"),
                    r.getString("jabatan")
                });
            }
            tableUser.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

    // ðŸ”¹ Tambah Data
    private void addUser(String nama, String username, String password, String jabatan) {
        try {
            Connection c = koneksi.getConnection();
            String sql = "INSERT INTO user (nama, username, password, jabatan) VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, username);
            p.setString(3, password);
            p.setString(4, jabatan);
            p.executeUpdate();
            JOptionPane.showMessageDialog(this, "User berhasil ditambahkan!");
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah user: " + e.getMessage());
        }
    }

    // ðŸ”¹ Update Data
    private void updateUser(int id, String nama, String username, String password, String jabatan) {
        try {
            Connection c = koneksi.getConnection();
            String sql = "UPDATE user SET nama=?, username=?, password=?, jabatan=? WHERE id=?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, username);
            p.setString(3, password);
            p.setString(4, jabatan);
            p.setInt(5, id);
            p.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data user berhasil diupdate!");
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
        }
    }

    // ðŸ”¹ Hapus Data
    private void deleteUser(int id) {
        try {
            Connection c = koneksi.getConnection();
            String sql = "DELETE FROM user WHERE id=?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setInt(1, id);
            p.executeUpdate();
            JOptionPane.showMessageDialog(this, "User berhasil dihapus!");
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
        }
    }

    // ðŸ”¹ Pencarian Data
    private void searchUser(String keyword) {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Jabatan");

        try {
            Connection c = koneksi.getConnection();
            String sql = "SELECT * FROM user WHERE nama LIKE ? OR username LIKE ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, "%" + keyword + "%");
            p.setString(2, "%" + keyword + "%");
            ResultSet r = p.executeQuery();

            while (r.next()) {
                model.addRow(new Object[]{
                    r.getInt("id"),
                    r.getString("nama"),
                    r.getString("username"),
                    r.getString("password"),
                    r.getString("jabatan")
                });
            }
            tableUser.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari: " + e.getMessage());
        }
    }

